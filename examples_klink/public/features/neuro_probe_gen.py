# Main assembly
# ═══════════════════════════════════════════════════════════════════════════════

def build_probe(c, cfg: ProbeConfig = None):
    if cfg is None:
        cfg = ProbeConfig()

    for L in range(1, 8):
        c.layer_ensure(L, 0)

    cell0 = c.cell_create(name="ProbeMain")["name"]
    cell120 = c.cell_create(name="ProbeInterface")["name"]

    build_cell120(c, cell120)
    build_l67_structure(c, cell0)
    build_coarse_bus(c, cell0)
    build_l1_fine_traces(c, cell0)
    build_l2_vias(c, cell0)
    build_l3_fine_traces(c, cell0)
    build_l3_pad_blocks(c, cell0)
    build_l4_interconnect(c, cell0)
    build_l4_fine_squares(c, cell0)
    build_l5_pads(c, cell0)

    c.instance_insert(cell0, cell120, position_dbu=[0, 0])
    c.show_cell(cell0)

    print(f"Done. ProbeMain={cell0!r}  ProbeInterface={cell120!r}")
    print(f"  electrodes = {cfg.num_electrodes} ({cfg.num_electrodes // 2} pairs)")
    print(f"  coarse traces = {len(BUS_YC_POS) * 2}")
    print(f"  pad columns = {cfg.pad_cols}")
    return cell0, cell120


